<div class='three_fifth'><h3>Origins</h3>
<p>Back in the late ’90s, three guys were working for a technology consulting company in Cambridge, Massachusetts. The trio soon learned they shared an interest in real estate. With dreams of becoming real estate moguls, they bought their first rental property together.</p>
<p>More purchases followed. Before long, they were losing track of which leases were expiring and which tenants hadn’t paid their rent — “minor” stuff like that. That’s when they began looking for property management software. They weren’t sure exactly what they needed, but it had to be easy to use, accessible anywhere with an Internet connection, and have all the features necessary to manage their properties.</p>
<p>After coming up empty-handed in their search, they decided to put their expensive computer science degrees to good use and build something for themselves. A few months later, Buildium was born.</p>
